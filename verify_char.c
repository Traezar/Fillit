#include "fillit.h"

int		verify_char(char *buf)
{
	int		i;

	i = 0;
	while (buf[i])
	{
		if (buf[i] != '#' && buf[i] != '.' && buf[i] != '\n')
			return (0);
		i++;
	}
	return (1);
}
